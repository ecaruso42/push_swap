#include "push_swap.h"

bool	stack_sorted(t_stack_node *stack)
{
	if(stack == NULL)
		return (1);
	while (stack->next)
	{
		if (stack->value > stack->next->value)
			return (false);
		stack = stack->next;
	}
	return (true);
}
